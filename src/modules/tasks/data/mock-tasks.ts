/** Split a flat seed into a Task template + (optionally) its first run. */
function toTask(seed: TaskSeed): Task {
  const task: Task = {
    id: seed.id,
    name: seed.name,
    type: seed.type,
    experimentId: seed.experimentId,
    experimentName: seed.experimentName,
    computeTarget: seed.computeTarget,
    engine: seed.engine,
    createdAt: seed.createdAt,
    description: seed.description,
    owner: seed.owner,
    priority: seed.priority,
    baseModel: seed.baseModel,
    dataset: seed.dataset,
    gpuRequired: seed.gpuRequired,
    runtime: seed.runtime,
    hyperparameters: seed.hyperparameters,
    runs: [],
  };

  // A "Draft" seed is a template that has never run → no runs.
  if (seed.status === "Draft") return task;

  task.runs = [
    {
      id: `${seed.id}-run-1`,
      taskId: seed.id,
      attempt: 1,
      status: seed.status,
      progress: seed.progress,
      createdAt: seed.createdAt,
      startedAt: seed.startedAt,
      finishedAt: seed.finishedAt,
      durationMs: seed.durationMs,
      outputStatus: seed.outputStatus,
      logs: seed.logs,
      artifacts: seed.artifacts,
      resourceUsage: seed.resourceUsage,
      timeline: seed.timeline,
    },
  ];
  return task;
}

export const initialMockTasks: Task[] = seeds.map(toTask);
