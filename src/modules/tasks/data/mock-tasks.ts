/**
 * Flat seed shape (config + one execution) kept for readability. We split each
 * seed into a Task template + its first run via {@link toTask} below.
 */
type TaskSeed = {
  id: string;
  name: string;
  type: TaskType;
  experimentId: string;
  experimentName: string;
  status: TaskStatus;
  progress: number;
  computeTarget: ComputeTarget;
  engine: EnginePlugin;
  createdAt: string;
  startedAt?: string;
  finishedAt?: string;
  durationMs: number;
  outputStatus: OutputStatus;
  description: string;
  owner: string;
  priority: TaskPriority;
  baseModel: string;
  dataset: string;
  gpuRequired: number;
  runtime: string;
  hyperparameters: TaskHyperparameters;
  logs: TaskLogEntry[];
  artifacts: TaskArtifact[];
  resourceUsage: TaskResourceUsage;
  timeline: TaskTimelineStep[];
};

function linkExperiment(experimentId: string): Pick<TaskSeed, "experimentId" | "experimentName"> {
  const experiment = getExperimentById(experimentId);
  return {
    experimentId,
    experimentName: experiment?.name ?? experimentId,
  };
}

